extension Commands.Profiles {
// ListResourceUsage checks the quota and actual usage of endpoints under the given CDN profile.
internal class ListResourceUsageCommand : BaseCommand, ProfilesListResourceUsage {
    var nextLink: String?
    public var hasAdditionalPages : Bool {
        get {
            return nextLink != nil
        }
    }
    public var resourceGroupName : String
    public var profileName : String
    public var subscriptionId : String
    public var apiVersion = "2017-04-02"

